    public List<Usuario> pegarDados(String usu) {
        Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;
        List<Usuario> usuario = new ArrayList<>();
        try {
            stmt = con.prepareStatement("SELECT * FROM usuario WHERE nome LIKE ?");
            stmt.setString(1, "%" + usu + "%");
            rs = stmt.executeQuery();
            while (rs.next()) {
                Usuario u = new Usuario();
                txtId.setText(rs.getString("id"));
                txtLogin.setText(rs.getString("login"));
                txtSenha.setText(rs.getString("senha"));
                usuario.add(u);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProdutoDAO.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            ConnectionFactory.ColseConnection(con, stmt, rs);

        }
        return usuario;

    }

