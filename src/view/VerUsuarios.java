    public void readJtable() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaUsuario.getModel();
        modelo.setNumRows(0);

        UsuarioDAO pdao = new UsuarioDAO();

        for (Usuario u : pdao.read()) {

            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getTelefone(),
                u.getEndereco(),
                u.getCpf(),
                u.getFuncao(),
                u.getData(),
                u.getLogin(),});
        }

    }
     public void readJtableForDesc(String nome) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaUsuario.getModel();
        modelo.setNumRows(0);

        UsuarioDAO udao = new UsuarioDAO();

        for (Usuario u : udao.readForDesc(nome)) {

            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getTelefone(),
                u.getEndereco(),
                u.getCpf(),
                u.getFuncao(),
                u.getData(),
                u.getLogin(),});
        }

    }

   

