    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (tabelaUsuario.getSelectedRow() != -1) {
            Usuario u = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();
            String valor = (String) cbFunced.getSelectedItem();

            u.setNome(txtNomed.getText());
            u.setTelefone(txtTelled.getText());
            u.setEndereco(txtEnded.getText());
            u.setCpf(txtCpfed.getText());
            u.setFuncao(valor);
            u.setData(txtDataed.getText());
            u.setLogin(txtLogined.getText());

            u.setId((int) tabelaUsuario.getValueAt(tabelaUsuario.getSelectedRow(), 0));
            dao.update(u);

            txtNomed.setText("");
            txtTelled.setText("");
            txtEnded.setText("");
            txtCpfed.setText("");
            cbFunced.setSelectedIndex(0);
            txtNomed.setText("");
            txtLogined.setText("");
            readJtable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para editar!");

        }

        readJtable();
    }//GEN-LAST:event_btnEditarActionPerformed

